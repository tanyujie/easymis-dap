package org.easymis.dap.catalog.entity;

public class Dbs {
	private String dbsId;
	private String name;
	private String dbsUri;
	private String host;
	private String dbsName;
	private String password;
	private String username;
	private String port;
	private String depict;
	private Integer status;



	public String getDbsId() {
		return dbsId;
	}

	public void setDbsId(String dbsId) {
		this.dbsId = dbsId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDbsUri() {
		return dbsUri;
	}

	public void setDbsUri(String dbsUri) {
		this.dbsUri = dbsUri;
	}

	public String getHost() {
		return host;
	}

	public void setHost(String host) {
		this.host = host;
	}

	public String getDbsName() {
		return dbsName;
	}

	public void setDbsName(String dbsName) {
		this.dbsName = dbsName;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getPort() {
		return port;
	}

	public void setPort(String port) {
		this.port = port;
	}

	public String getDepict() {
		return depict;
	}

	public void setDepict(String depict) {
		this.depict = depict;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

}
